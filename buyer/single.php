							<h3>History</h3>
								<P>Online auctions were taking place even before the release of the first web browser for personal computers, NCSA Mosaic. Instead of users selling items through the Web they were instead trading through text-based newsgroups and email discussion lists. However, the first Web-based commercial activity regarding online auctions that made significant sales began in May 1995 with the company Onsale. In September that same year eBay also began trading. Both of these companies used ascending bid. The Web offered new advantages such as the use of automated bids via electronic forms, a search engine to be able to quickly find items and the ability to allow users to view items by categories.

								Online auctions have greatly increased the variety of goods and services that can be bought and sold using auction mechanisms along with expanding the possibilities for the ways auctions can be conducted and in general created new uses for auctions. In the current web environment there are hundreds, if not thousands, of websites dedicated to online auction practices.
								</P>
