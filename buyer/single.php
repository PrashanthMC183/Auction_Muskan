							<h3>Legalities</h3><br>
							<h4><u>Shill Bidding</u></h4>
								<P>Placing fake bids that benefits the seller of the item is known as shill bidding. This is a method often used in Online auctions but can also happen in standard auctions. This is seen as an unlawful act as it unfairly raises the final price of the auction, so that the winning bidder pays more than they should have. If the shill bid is unsuccessful, the item owner needs to pay the auction fees. In 2011, a member of eBay became the first individual to be convicted of shill bidding on an auction.By taking part in the process, an individual is breaking the European Union fair trading rules which carries out a fine of up to £5,000 in the United Kingdom.
								</P>
							<h4><u>Fraud</u></h4>
							<p>The increasing popularity of using online auctions has led to an increase in fraudulent activity.This is usually performed on an auction website by creating a very appetising auction, such as a low starting amount. Once a buyer wins an auction and pays for it, the fradulent seller will either not pursue with the delivery,or send a less valuable version of the purchased item (replicated, used, refurbished, etc.). Protection to prevent such acts has become readily available, most notably Paypal's buyer protection policy. As Paypal handles the transaction, they have the ability to hold funds until a conclusion is drawn whereby the victim can be compensated.</p>
							<h4><u>Sale of stolen goods</u></h4>
							<p>Online auction websites are used by thieves or fences to sell stolen goods to unsuspecting buyers. According to police statistics there were over 8000 crimes involving stolen goods, fraud or deception reported on eBay in 2009.It has become common practice for organised criminals to steal in-demand items, often in bulk. These items are then sold online as it is a safer option due to the anonymity and worldwide market it provides.Auction fraud makes up a large percentage of complaints received by the FBI’s Internet Crime Complaint Center (IC3). This was around 45% in 2006 and 63% in 2005.</p>
